let getsortedcity = async function (req,res){
    try{

        let city =["benglure","mumbai","delhi","nashik"]
        let cityobj = []
        for (i=0;i<city.length;i++){
            let obj = { city:city[i]}
            let resp = await axios.get(`http://api.openweathermap.org/data/2.5/weather?q=${city}&appid= c5a07a5f1ba8b10f0e5cb28b018f097a `)
            console.log(resp.date.main.temp)
            obj.temp = resp.date.main.temp
            cityobj.push(obj)
        }

        let sorted = cityobj.sort(function (a,b){return a.temp- b.temp})
        console.log(sorted)
        res.status(200).send({status:true, data:sorted})
    }
    catch (error){
        console.log(error)
        res.status(500).send({status:false, msg:"server error"})

    }
}
